@cast_bp.route('/api/cast', methods=['GET'])
def get_cast():
    """Возвращает базовый список участников каста"""
    return jsonify({"success": True, "data": CAST_DATA})


@cast_bp.route('/api/cast/<cast_id>', methods=['GET'])
def get_cast_member(cast_id):
    """Возвращает полные данные участника каста для страницы"""
    try:
        member = None
        
        if cast_id.isdigit():
            member = next((c for c in CAST_DATA if c['id'] == int(cast_id)), None)
        
        if not member:
            target_route = f'/cast/{cast_id}'
            member = next((c for c in CAST_DATA if c.get('route') == target_route), None)
        
        if not member:
            return jsonify({
                "success": False, 
                "message": f"Участник '{cast_id}' не найден"
            }), 404
        
        return jsonify({"success": True, "data": member})
    
    except Exception as e:
        print(f"ERROR in get_cast_member('{cast_id}'): {e}")
        import traceback
        traceback.print_exc()
